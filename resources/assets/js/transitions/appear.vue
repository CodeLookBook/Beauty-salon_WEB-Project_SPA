<template lang="pug">

    transition( appear
                @before-enter="beforeEnter",
                @enter="enter",
                @leave="leave")
        slot

</template>
<style lang="scss">


    //BREAKPOINTS +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++


    //STRUCTURE  ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++


</style>
<script>

    //*************************************************************************
    // CHILD COMPONENTS IMPORT
    //*************************************************************************


    //*************************************************************************
    // COMPONENT
    //*************************************************************************

    export default {

        //*********************************************************************
        // PROPERTIES
        //*********************************************************************


        //*********************************************************************
        // DATA FIELDS
        //*********************************************************************


        //*********************************************************************
        // COMPUTED FIELDS
        //*********************************************************************


        //*********************************************************************
        // WATCHED FIELDS
        //*********************************************************************


        //*********************************************************************
        // METHODS
        //*********************************************************************

        methods:{
            beforeEnter(el){
                el.style.opacity = 0;
            },
            enter(el, done){
                Velocity(el,{
                    opacity: 1,
                },{
                    duration: 1500,
                    complete: done,
                });
            },
            leave(el,done){
                Velocity(el,{
                    opacity: 0,
                }, {
                    duration: 500,
                    complete: done,
                });
            }
        }

        //*********************************************************************
        // LIFE HOOKS
        //*********************************************************************


        //*********************************************************************
        // CHILD COMPONENTS
        //*********************************************************************


    };

</script>