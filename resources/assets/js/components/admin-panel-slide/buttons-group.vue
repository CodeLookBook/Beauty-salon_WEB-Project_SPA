<template lang="pug">

    //abbreviation 'AP' - is Admin Pannel

    .row.AP-BUTTONS-GROUP
        .col-xs-12
            .row.GROUP-NAME-SLOT(@click="ToggleButtonsSlotVisibility",)
                slot(name="name")
        .col-xs-12
            transition(name="OPEN-DOWN-TRANSITION")
                .row.BUTTONS-SLOT(v-show="buttonsSlot.isShown" ref="buttonsSlot")
                    slot

</template>
<style lang="scss">

    @import "../../../sass/mixins";
    //DECOR: +++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

</style>
<script>

    //*************************************************************************
    // CHILD COMPONENTS IMPORT
    //*************************************************************************


    //*************************************************************************
    // COMPONENT
    //*************************************************************************

    export default {

        //*********************************************************************
        // PROPERTIES
        //*********************************************************************

        props:{
            isShown:{
                type: Boolean,
                default: false,
            },
        },

        //*********************************************************************
        // DATA FIELDS
        //*********************************************************************

        data(){
            return {
                buttonsSlot:{
                    isShown: this.isShown,
                    height: 0,
                }
            };
        },

        //*********************************************************************
        // COMPUTED FIELDS
        //*********************************************************************


        //*********************************************************************
        // WATCHED FIELDS
        //*********************************************************************


        //*********************************************************************
        // METHODS
        //*********************************************************************

        methods:{
            ToggleButtonsSlotVisibility(){
                let v = this,
                    s = v.buttonsSlot;

                s.isShown = !s.isShown;
            },
        },

        //*********************************************************************
        // LIFE HOOKS
        //*********************************************************************


        //*********************************************************************
        // CHILD COMPONENTS
        //*********************************************************************


    };

</script>